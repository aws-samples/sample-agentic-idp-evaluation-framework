/** Build method→capabilities map from comparison best method, pipeline results, or static fallback */
function buildMethodMap(capabilities: Capability[], processingResults: ProcessorResult[], comparison?: ComparisonResult | null): Map<string, string[]> {
  const methodMap = new Map<string, string[]>();

  // Priority 1: Use comparison's best method per capability (reflects actual benchmark winner)
  if (comparison?.capabilityMatrix) {
    for (const cap of capabilities) {
      const matrix = comparison.capabilityMatrix[cap];
      if (!matrix) continue;
      // Find the method with highest confidence for this capability
      let bestMethod = '';
      let bestConf = -1;
      for (const [method, data] of Object.entries(matrix)) {
        const conf = (data as { confidence?: number })?.confidence ?? 0;
        if (conf > bestConf) {
          bestConf = conf;
          bestMethod = method;
        }
      }
      if (bestMethod && bestConf > 0) {
        if (!methodMap.has(bestMethod)) methodMap.set(bestMethod, []);
        methodMap.get(bestMethod)!.push(cap);
      }
    }
  }

  // Priority 2: Fill unmapped capabilities from pipeline execution results
  const mappedCaps = new Set(Array.from(methodMap.values()).flat());
  if (processingResults.length > 0) {
    for (const result of processingResults) {
      if (result.status !== 'complete') continue;
      const caps = Object.keys(result.results).filter(c => capabilities.includes(c as Capability) && !mappedCaps.has(c));
      for (const cap of caps) {
        if (!methodMap.has(result.method)) methodMap.set(result.method, []);
        methodMap.get(result.method)!.push(cap);
        mappedCaps.add(cap);
      }
    }
  }

  // Priority 3: Static fallback for any remaining
  for (const cap of capabilities) {
    if (mappedCaps.has(cap)) continue;
    const methods = getBestMethodsForCapability(cap);
    const best = methods[0];
    if (!methodMap.has(best)) methodMap.set(best, []);
    methodMap.get(best)!.push(cap);
  }

  return methodMap;
}

function generatePythonCode(capabilities: Capability[], processingResults: ProcessorResult[], comparison?: ComparisonResult | null): string {
  const methodMap = buildMethodMap(capabilities, processingResults, comparison);

  const imports = [
    'import boto3',
    'import json',
    'from typing import Any',
    '',
  ];

  const lines = [
    ...imports,
    '# IDP Pipeline - Auto-generated by ONE IDP Platform',
    `# Capabilities: ${capabilities.map(c => c.replace(/_/g, ' ')).join(', ')}`,
    `# Methods: ${Array.from(methodMap.keys()).map(m => METHOD_INFO[m as ProcessingMethod].shortName).join(', ')}`,
    '',
    'bedrock = boto3.client("bedrock-runtime", region_name="us-west-2")',
    '',
    '',
    'def process_document(document_bytes: bytes, document_name: str) -> dict[str, Any]:',
    '    """Process a document through the IDP pipeline."""',
    '    results = {}',
    '',
  ];

  for (const [method, caps] of methodMap.entries()) {
    const info = METHOD_INFO[method as ProcessingMethod];
    const capList = caps.map(c => `"${c}"`).join(', ');

    if (info.family === 'claude' || info.family === 'nova') {
      const modelId = method === 'claude-sonnet' ? 'us.anthropic.claude-sonnet-4-6'
        : method === 'claude-haiku' ? 'us.anthropic.claude-haiku-4-5-20251001-v1:0'
        : method === 'claude-opus' ? 'us.anthropic.claude-opus-4-6-v1'
        : method === 'nova-lite' ? 'us.amazon.nova-2-lite-v1:0'
        : 'us.amazon.nova-2-pro-preview-20251202-v1:0';

      lines.push(
        `    # --- ${info.shortName} (${info.family}) ---`,
        `    # Capabilities: ${caps.map(c => c.replace(/_/g, ' ')).join(', ')}`,
        `    # Pricing: $${info.tokenPricing.inputPer1MTokens}/M input, $${info.tokenPricing.outputPer1MTokens}/M output`,
        `    prompt_${method.replace(/-/g, '_')} = f"""Extract the following from this document:`,
        `    Capabilities: ${caps.map(c => c.replace(/_/g, ' ')).join(', ')}`,
        `    Return structured JSON with each capability as a key."""`,
        '',
        `    response_${method.replace(/-/g, '_')} = bedrock.converse(`,
        `        modelId="${modelId}",`,
        '        messages=[{',
        '            "role": "user",',
        '            "content": [',
        '                {"document": {"name": document_name, "format": "pdf",',
        '                              "source": {"bytes": document_bytes}}},',
        `                {{"text": prompt_${method.replace(/-/g, '_')}}}`,
        '            ]',
        '        }],',
        '        inferenceConfig={"maxTokens": 4096, "temperature": 0.1}',
        '    )',
        '',
        `    text_${method.replace(/-/g, '_')} = response_${method.replace(/-/g, '_')}["output"]["message"]["content"][0]["text"]`,
        `    usage_${method.replace(/-/g, '_')} = response_${method.replace(/-/g, '_')}["usage"]`,
        `    results["${method}"] = {`,
        `        "model": "${modelId}",`,
        `        "capabilities": [${capList}],`,
        `        "extraction": json.loads(text_${method.replace(/-/g, '_')}),`,
        `        "tokens": usage_${method.replace(/-/g, '_')},`,
        `        "cost": (usage_${method.replace(/-/g, '_')}["inputTokens"] / 1_000_000 * ${info.tokenPricing.inputPer1MTokens})`,
        `             + (usage_${method.replace(/-/g, '_')}["outputTokens"] / 1_000_000 * ${info.tokenPricing.outputPer1MTokens})`,
        '    }',
        '',
      );
    } else if (info.family === 'bda') {
      lines.push(
        `    # --- ${info.shortName} (Bedrock Data Automation) ---`,
        `    # Capabilities: ${caps.map(c => c.replace(/_/g, ' ')).join(', ')}`,
        `    # Pricing: $${info.estimatedCostPerPage}/page`,
        '    bda_client = boto3.client("bedrock-data-automation-runtime")',
        '    # BDA requires S3 input - upload document first',
        '    # bda_response = bda_client.invoke_data_automation_async(...)',
        `    results["${method}"] = {"capabilities": [${capList}], "status": "bda_async"}`,
        '',
      );
    } else if (info.family === 'textract-llm') {
      lines.push(
        `    # --- ${info.shortName} (Textract + LLM) ---`,
        `    # Capabilities: ${caps.map(c => c.replace(/_/g, ' ')).join(', ')}`,
        '    textract = boto3.client("textract")',
        '    # Step 1: OCR with Textract',
        '    textract_response = textract.analyze_document(',
        '        Document={"Bytes": document_bytes},',
        '        FeatureTypes=["TABLES", "FORMS", "SIGNATURES"]',
        '    )',
        '    ocr_text = " ".join(b["Text"] for b in textract_response["Blocks"] if b["BlockType"] == "LINE")',
        '',
        '    # Step 2: Structure with LLM',
        `    llm_response = bedrock.converse(`,
        `        modelId="${method.includes('sonnet') ? 'us.anthropic.claude-sonnet-4-6' : method.includes('haiku') ? 'us.anthropic.claude-haiku-4-5-20251001-v1:0' : 'us.amazon.nova-2-lite-v1:0'}",`,
        '        messages=[{"role": "user", "content": [{"text": f"Extract structured data from OCR output: {ocr_text}"}]}],',
        '        inferenceConfig={"maxTokens": 4096}',
        '    )',
        `    results["${method}"] = {"capabilities": [${capList}], "ocr": ocr_text, "structured": llm_response}`,
        '',
      );
    }
  }

  lines.push(
    '    return results',
    '',
    '',
    'if __name__ == "__main__":',
    '    with open("sample.pdf", "rb") as f:',
    '        doc_bytes = f.read()',
    '    result = process_document(doc_bytes, "sample.pdf")',
    '    print(json.dumps(result, indent=2, default=str))',
  );

  return lines.join('\n');
}

function generateTypeScriptCode(capabilities: Capability[], processingResults: ProcessorResult[], comparison?: ComparisonResult | null): string {
  const methodMap = buildMethodMap(capabilities, processingResults, comparison);

  const lines = [
    '// IDP Pipeline - Auto-generated by ONE IDP Platform',
    `// Capabilities: ${capabilities.map(c => c.replace(/_/g, ' ')).join(', ')}`,
    '',
    'import { BedrockRuntimeClient, ConverseCommand } from "@aws-sdk/client-bedrock-runtime";',
    'import { readFileSync } from "fs";',
    '',
    'const client = new BedrockRuntimeClient({ region: "us-west-2" });',
    '',
    'interface ExtractionResult {',
    '  model: string;',
    '  capabilities: string[];',
    '  extraction: Record<string, unknown>;',
    '  tokens: { inputTokens: number; outputTokens: number };',
    '  cost: number;',
    '}',
    '',
    'async function processDocument(documentBytes: Buffer, fileName: string): Promise<Record<string, ExtractionResult>> {',
    '  const results: Record<string, ExtractionResult> = {};',
    '',
  ];

  for (const [method, caps] of methodMap.entries()) {
    const info = METHOD_INFO[method as ProcessingMethod];
    const modelId = method === 'claude-sonnet' ? 'us.anthropic.claude-sonnet-4-6'
      : method === 'claude-haiku' ? 'us.anthropic.claude-haiku-4-5-20251001-v1:0'
      : method === 'nova-lite' ? 'us.amazon.nova-2-lite-v1:0'
      : method === 'nova-pro' ? 'us.amazon.nova-2-pro-preview-20251202-v1:0'
      : 'us.anthropic.claude-sonnet-4-6';

    if (info.family === 'claude' || info.family === 'nova') {
      lines.push(
        `  // ${info.shortName} — ${caps.map(c => c.replace(/_/g, ' ')).join(', ')}`,
        `  const resp_${method.replace(/-/g, '_')} = await client.send(new ConverseCommand({`,
        `    modelId: "${modelId}",`,
        '    messages: [{',
        '      role: "user",',
        '      content: [',
        `        { document: { name: fileName, format: "pdf", source: { bytes: documentBytes } } },`,
        `        { text: "Extract: ${caps.map(c => c.replace(/_/g, ' ')).join(', ')}. Return JSON." }`,
        '      ]',
        '    }],',
        '    inferenceConfig: { maxTokens: 4096, temperature: 0.1 }',
        '  }));',
        '',
        `  const text_${method.replace(/-/g, '_')} = resp_${method.replace(/-/g, '_')}.output?.message?.content?.[0]?.text ?? "";`,
        `  const usage_${method.replace(/-/g, '_')} = resp_${method.replace(/-/g, '_')}.usage!;`,
        `  results["${method}"] = {`,
        `    model: "${modelId}",`,
        `    capabilities: ${JSON.stringify(caps)},`,
        `    extraction: JSON.parse(text_${method.replace(/-/g, '_')}),`,
        `    tokens: { inputTokens: usage_${method.replace(/-/g, '_')}.inputTokens!, outputTokens: usage_${method.replace(/-/g, '_')}.outputTokens! },`,
        `    cost: (usage_${method.replace(/-/g, '_')}.inputTokens! / 1_000_000 * ${info.tokenPricing.inputPer1MTokens})`,
        `         + (usage_${method.replace(/-/g, '_')}.outputTokens! / 1_000_000 * ${info.tokenPricing.outputPer1MTokens})`,
        '  };',
        '',
      );
    }
  }

  lines.push(
    '  return results;',
    '}',
    '',
    '// Run',
    'const doc = readFileSync("sample.pdf");',
    'processDocument(doc, "sample.pdf").then(r => console.log(JSON.stringify(r, null, 2)));',
  );

  return lines.join('\n');
}

function generateCDKCode(capabilities: Capability[]): string {
  return [
    '// CDK Stack - IDP Pipeline Infrastructure',
    '// Auto-generated by ONE IDP Platform',
    '',
    'import * as cdk from "aws-cdk-lib";',
    'import * as s3 from "aws-cdk-lib/aws-s3";',
    'import * as lambda from "aws-cdk-lib/aws-lambda-nodejs";',
    'import * as iam from "aws-cdk-lib/aws-iam";',
    'import * as stepfunctions from "aws-cdk-lib/aws-stepfunctions";',
    'import { Construct } from "constructs";',
    '',
    'export class IdpPipelineStack extends cdk.Stack {',
    '  constructor(scope: Construct, id: string, props?: cdk.StackProps) {',
    '    super(scope, id, props);',
    '',
    '    // S3 bucket for document storage',
    '    const bucket = new s3.Bucket(this, "DocumentBucket", {',
    '      versioned: true,',
    '      encryption: s3.BucketEncryption.S3_MANAGED,',
    '      removalPolicy: cdk.RemovalPolicy.RETAIN,',
    '    });',
    '',
    '    // Lambda for document processing',
    '    const processor = new lambda.NodejsFunction(this, "Processor", {',
    '      entry: "lambda/processor.ts",',
    '      handler: "handler",',
    '      runtime: lambda.Runtime.NODEJS_20_X,',
    '      timeout: cdk.Duration.minutes(5),',
    '      memorySize: 1024,',
    '      environment: {',
    '        BUCKET_NAME: bucket.bucketName,',
    `        CAPABILITIES: "${capabilities.join(",")}",`,
    '      },',
    '    });',
    '',
    '    // Grant Bedrock access',
    '    processor.addToRolePolicy(new iam.PolicyStatement({',
    '      actions: ["bedrock:InvokeModel", "bedrock:InvokeModelWithResponseStream"],',
    '      resources: ["*"],',
    '    }));',
    '',
    '    bucket.grantReadWrite(processor);',
    '',
    '    new cdk.CfnOutput(this, "BucketName", { value: bucket.bucketName });',
    '  }',
    '}',
  ].join('\n');
}

