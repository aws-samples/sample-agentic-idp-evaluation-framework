export function generateCdkLambdaHandler(
  capabilities: Capability[],
  processingResults: ProcessorResult[],
  comparison?: ComparisonResult | null,
): string {
  const methodMap = buildMethodMap(capabilities, processingResults, comparison);
  return `import { randomUUID } from 'node:crypto';
import { basename } from 'node:path';
import type { APIGatewayProxyEventV2, APIGatewayProxyResultV2, S3Event } from 'aws-lambda';
import { S3Client, GetObjectCommand } from '@aws-sdk/client-s3';
import { DynamoDBClient } from '@aws-sdk/client-dynamodb';
import { DynamoDBDocumentClient, PutCommand } from '@aws-sdk/lib-dynamodb';
import { processDocument } from '../process.js';

const REGION = process.env.AWS_REGION ?? 'us-west-2';
const s3 = new S3Client({ region: REGION, maxAttempts: 5 });
const ddb = DynamoDBDocumentClient.from(new DynamoDBClient({ region: REGION, maxAttempts: 5 }));

const RESULTS_TABLE = process.env.RESULTS_TABLE!;
const INPUT_BUCKET = process.env.INPUT_BUCKET!;

async function bytesFromS3(bucket: string, key: string): Promise<{ bytes: Buffer; fileName: string; s3Uri: string }> {
  const out = await s3.send(new GetObjectCommand({ Bucket: bucket, Key: key }));
  const bytes = Buffer.from(await out.Body!.transformToByteArray());
  return { bytes, fileName: basename(key), s3Uri: \`s3://\${bucket}/\${key}\` };
}

async function persist(documentId: string, methodId: string, payload: unknown): Promise<void> {
  await ddb.send(new PutCommand({
    TableName: RESULTS_TABLE,
    Item: {
      documentId,
      methodId,
      createdAt: new Date().toISOString(),
      payload,
    },
  }));
}

type Event = APIGatewayProxyEventV2 | S3Event;

export async function handler(event: Event): Promise<APIGatewayProxyResultV2 | void> {
  // S3 trigger: process all new objects.
  if ('Records' in event) {
    for (const record of event.Records) {
      const bucket = record.s3.bucket.name;
      const key = decodeURIComponent(record.s3.object.key.replace(/\\+/g, ' '));
      const { bytes, fileName, s3Uri } = await bytesFromS3(bucket, key);
      const documentId = \`\${randomUUID()}\`;
      const result = await processDocument(bytes, fileName, s3Uri);
      for (const [methodId, method] of Object.entries(result.methods)) {
        await persist(documentId, methodId, method);
      }
    }
    return;
  }

  // API Gateway: { fileName, s3Key?, bodyBase64? } in JSON.
  const body = event.body ? JSON.parse(event.isBase64Encoded ? Buffer.from(event.body, 'base64').toString('utf-8') : event.body) : {};
  const fileName = body.fileName ?? 'document.pdf';

  let bytes: Buffer;
  let s3Uri: string | undefined;
  if (body.s3Key) {
    const fetched = await bytesFromS3(INPUT_BUCKET, body.s3Key);
    bytes = fetched.bytes;
    s3Uri = fetched.s3Uri;
  } else if (body.bodyBase64) {
    bytes = Buffer.from(body.bodyBase64, 'base64');
  } else {
    return { statusCode: 400, body: JSON.stringify({ error: 'must provide s3Key or bodyBase64' }) };
  }

  const documentId = \`\${randomUUID()}\`;
  const result = await processDocument(bytes, fileName, s3Uri);
  for (const [methodId, method] of Object.entries(result.methods)) {
    await persist(documentId, methodId, method);
  }

  return {
    statusCode: 200,
    headers: { 'content-type': 'application/json' },
    body: JSON.stringify({ documentId, result }),
  };
}
`;
}

