function computeCost(method: string, inTok: number, outTok: number, pages = 1): number {
  const p = PRICING[method];
  const token = (inTok / 1_000_000) * p.in + (outTok / 1_000_000) * p.out;
  const page = p.perPage * pages;
  return Math.round((token + page) * 1_000_000) / 1_000_000;
}

function parseYamlDefensive(raw: string): Record<string, unknown> {
  const candidates = [
    raw.trim(),
    raw.replace(/\`\`\`(?:yaml|yml)?/gi, '').replace(/\`\`\`/g, '').trim(),
    raw.match(/\\{[\\s\\S]*\\}/)?.[0] ?? '',
  ];
  for (const c of candidates) {
    if (!c) continue;
    try {
      const parsed = YAML.parse(c);
      if (parsed && typeof parsed === 'object') return parsed as Record<string, unknown>;
    } catch { /* try next */ }
    try {
      const parsed = JSON.parse(c);
      if (parsed && typeof parsed === 'object') return parsed as Record<string, unknown>;
    } catch { /* try next */ }
  }
  return {};
}

function coerceCap(val: unknown): CapabilityResult {
  if (val && typeof val === 'object' && 'data' in (val as Record<string, unknown>)) {
    const v = val as Record<string, unknown>;
    return {
      data: v.data,
      confidence: typeof v.confidence === 'number' ? v.confidence : 0.8,
      format: ((v.format as CapabilityResult['format']) ?? 'json'),
    };
  }
  if (val == null) return { data: null, confidence: 0, format: 'text' };
  return { data: val, confidence: 0.8, format: 'json' };
}

async function buildContentBlock(docBytes: Buffer, fileName: string): Promise<ContentBlock> {
  const ext = extname(fileName).toLowerCase().slice(1);
  if (ext === 'pdf') {
    return { document: { name: 'document', format: 'pdf', source: { bytes: docBytes } } };
  }
  const formatMap: Record<string, 'jpeg' | 'png' | 'gif' | 'webp'> = {
    jpg: 'jpeg', jpeg: 'jpeg', png: 'png', gif: 'gif', webp: 'webp',
    tiff: 'jpeg', tif: 'jpeg', bmp: 'jpeg',
  };
  const fmt = formatMap[ext] ?? 'jpeg';
  let bytes = docBytes;
  if (bytes.length > 4.5 * 1024 * 1024) {
    let pipeline = sharp(bytes).resize({ width: 2000, withoutEnlargement: true });
    pipeline = fmt === 'png' ? pipeline.png({ compressionLevel: 8 }) : pipeline.jpeg({ quality: 80 });
    bytes = await pipeline.toBuffer();
  }
  return { image: { format: fmt, source: { bytes } } };
}

